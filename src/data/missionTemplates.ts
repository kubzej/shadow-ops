export type MissionCategory =
  | 'surveillance'
  | 'cyber'
  | 'extraction'
  | 'sabotage'
  | 'influence'
  | 'finance'
  | 'logistics'
  | 'blackops'
  | 'counter';
export type MissionDifficulty = 1 | 2 | 3 | 4 | 5;

export interface MissionTarget {
  id: string;
  name: string;
  category: MissionCategory;
  description: string;
  baseRewardMoney: number;
  baseRewardIntel: number;
  baseRewardShadow: number;
  baseRewardInfluence: number;
  alertGain: number;
  discretion: number; // 1-5, lower = more alert gain on exposure
  intelCost?: number; // intel required to dispatch
  chainNextTargetId?: string; // on success, auto-generate follow-up mission
}

export interface MissionComplication {
  id: string;
  description: string;
  difficultyMod: number;
  durationMod: number;
  requiresCombat?: boolean;
  requiresTech?: boolean;
  requiresStealth?: boolean;
  requiresIntel?: boolean;
}

